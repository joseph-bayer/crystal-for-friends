;    * If TRAINERTYPE_FORM is present:  db form
;    * If TRAINERTYPE_ITEM is present	dw item
;    * If TRAINERTYPE_MOVES is present: dw move 1
;                                  	dw move 2
;                                  	dw move 3
;                                  	dw move 4
; - db -1 ; end


